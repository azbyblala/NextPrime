public class Graph {
    public static void main(String[] args) {
        int i,j;
        for(i=1;i<=9;i++){
            for(j=1;j<=9;j++)
                System.out.print(i+"*"+j+"="+(i*j)+"\t");//斜t是跳格的意思
        System.out.print("\n");
        }
        }
        }
